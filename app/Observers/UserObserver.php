     * Handle the user "created" event.
     *
     * @param  \App\Models\User  $user
     * @return void
     */
    public function created(User $user)
    {
        $userId = $user->id;
        if (!Role::create(['user_id' => $userId])){
            $user->delete();
            Log::error('user|create_role_failed',['data' => $user]);
            session()->flash('danger','系统错误，请联系管理员');
        }
    }

